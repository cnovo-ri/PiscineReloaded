int		ft_iterative_factorial(int nb)
{
	int f;
	int i;

	if (nb >= 0 && nb <= 12)
	{
		f = 1;
		i = 1;
		while (i <= nb)
		{
			f *= i;
			i++;
		}
		return (f);
	}
	else
		return (0);
}
